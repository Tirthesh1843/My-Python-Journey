def double(x):
    return x * 2

print(double(5))


MulBy2 = lambda x: x * 2  # Define a lambda function that takes one argument and returns its double
print(MulBy2(5))  # Call the lambda function with the argument 5
Average = lambda a, b: (a + b) / 2  # Define a lambda function that takes two arguments and returns their average
print(Average(10, 20))  # Call the lambda function with the arguments 10 and 20
Square = lambda x: x ** 2  # Define a lambda function that takes one argument
print(Square(5))  # Call the lambda function with the argument 5