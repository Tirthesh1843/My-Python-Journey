#there are 4 pillars of OOPS
# 1. Encapsulation
# 2. Abstraction
# 3. Inheritance
# 4. Polymorphism

# encapsulation

#encapsulation is a way of bundling data and methods that work on that data into a single unit called class. This helps to hide the internal implementation details of a class and make it easier to use.

#Abstraction

# abstraction is a way of hiding the implementation details of a class and showing only the functionality of the class to the user. This helps to simplify the code and make it easier to use.

#Inheritance

# inheritance is a way of creating a new class that is a subclass of an existing class. This allows the new class to inherit the functionality of the existing class and add new functionality to it.

#Polymorphism

# polymorphism is a way of defining multiple forms for a single object. This allows the object to be used in different ways depending on the context. This helps to make the code more flexible and easier to use.